describe('lib/solidity/coder', () => {
    describe('encodeParameter', () => {
        _.each(paramTests, (t) => {
            it(`should turn ${t.value} to ${t.expected}`, () => {
                const expected = t.expected.join('');
                assert.equal(coder.encodeParameter(t.type, t.value).replace('0x', ''), expected);
            });
        });
    });

    describe('encodeParameters', () => {
        _.each(paramsTests, (t) => {
            it(`should turn ${t.values} to ${t.expected}`, () => {
                const expected = t.expected.join('');
                assert.equal(coder.encodeParameters(t.types, t.values).replace('0x', ''), expected);
            });
        });
    });
});
